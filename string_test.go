package hexcolor

import (
	"image/color"
	"testing"

	"github.com/stretchr/testify/assert"
)

func TestColorToString(t *testing.T) {
	var tl = map[string]color.NRGBA{
		"#FFFFFF":   {0xFF, 0xFF, 0xFF, 0xFF},
		"#FF0000":   {0xFF, 0x00, 0x00, 0xFF},
		"#0000FF":   {0x00, 0x00, 0xFF, 0xFF},
		"#FF00FF":   {0xFF, 0x00, 0xFF, 0xFF},
		"#000FFF":   {0x00, 0x0F, 0xFF, 0xFF},
		"#FFF000":   {0xFF, 0xF0, 0x00, 0xFF},
		"#F0F00F":   {0xF0, 0xF0, 0x0F, 0xFF},
		"#123456":   {0x12, 0x34, 0x56, 0xFF},
		"#FFFFFF23": {0xFF, 0xFF, 0xFF, 0x23},
		"#12345678": {0x12, 0x34, 0x56, 0x78},
	}

	for s, clr := range tl {
		var s1 = ToString(clr)
		assert.Equal(t, SafeParse(s1), clr, "ERROR: [ %s <= %s ]", s1, s)
	}
}
